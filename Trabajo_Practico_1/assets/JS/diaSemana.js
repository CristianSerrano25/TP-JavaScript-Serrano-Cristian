let dia = parseInt(prompt("Ingrese un numero del 1 al 7"));

switch (dia) {
    case 1: dia == 1;
        console.log("El dia es lunes");

        break;
    case 2: dia == 2;
        console.log("El dia es martes");

        break;
    case 3: dia == 3;
        console.log("El dia es miércoles");

        break;
    case 4: dia == 4;
        console.log("El dia es jueves");

        break;
    case 5: dia == 5;
        console.log("El dia es viernes");

        break;
    case 6: dia == 6;
        console.log("El dia es sábado");

        break;
    case 7: dia == 7;
        console.log("El dia es domingo");

        break;
    default:
        console.log("El numero ingresado no correspode a un dia de la semana");
        break;
}